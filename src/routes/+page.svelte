	<h2>Getting Started</h2>
	<p>
		SvelteKit is a framework for building web applications of all sizes, with a beautiful
		development experience and flexible filesystem-based routing. It's built on top of Svelte, a
		compile-time web component framework that generates highly optimized JavaScript.
	</p>

	<h3>Prerequisites</h3>
	<p>Before diving into SvelteKit, you should have:</p>
	<ul>
		<li>Basic knowledge of HTML, CSS, and JavaScript</li>
		<li>Familiarity with modern JavaScript (ES6+)</li>
		<li>Understanding of component-based architecture</li>
		<li>Experience with package managers like npm or yarn</li>
	</ul>

	<h3>Installation</h3>
	<p>
		Getting started with SvelteKit is straightforward. You can create a new project using the
